package com.sousa.batch.model;

public class Codigo {
	private String codigo;
	private String codigoantigo;
	private int idpecas;
	
	public String getCodigo() {
		return codigo;
	}
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	public String getCodigoantigo() {
		return codigoantigo;
	}
	public void setCodigoantigo(String codigoantigo) {
		this.codigoantigo = codigoantigo;
	}
	public int getIdpecas() {
		return idpecas;
	}
	public void setIdpecas(int idpecas) {
		this.idpecas = idpecas;
	}
}
